/**
 * 공백으로 토큰화 → 사전 룩업 → 미매칭 토큰은 원문 유지 → 공백으로 재결합.
 * 검색 엔진은 보통 공백 분리 AND 매칭이라 부분 번역이어도 결과 품질이 크게 떨어지지 않음.
 */
export function translateKoToJa(text: string): string {
  if (!text) return text;
  return text
    .trim()
    .split(/\s+/)
    .map((tok) => DICT[tok] ?? tok)
    .join(' ');
}
