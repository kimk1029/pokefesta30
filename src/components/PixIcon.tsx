export function PixIcon({ d, size = 18 }: { d: string[]; size?: number }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 10 10"
      style={{ shapeRendering: 'crispEdges' }}
    >
      {d.map((row, y) =>
        row.split('').map((c, x) =>
          c === '1' ? (
            <rect key={`${x}-${y}`} x={x} y={y} width="1" height="1" fill="currentColor" />
          ) : null,
        ),
      )}
    </svg>
  );
}

