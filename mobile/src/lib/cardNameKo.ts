/** 직전 한글 음절에 받침을 합성. final: ㄴ=4, ㅁ=16. */
function appendBatchim(out: string, final: number): string {
  if (out.length === 0) return out;
  const lastChar = out[out.length - 1];
  const code = lastChar.charCodeAt(0);
  // AC00..D7A3 = 완성형 한글
  if (code < 0xac00 || code > 0xd7a3) return out + (final === 4 ? 'ㄴ' : 'ㅁ');
  const base = code - 0xac00;
  const existingFinal = base % 28;
  if (existingFinal !== 0) return out + (final === 4 ? 'ㄴ' : 'ㅁ'); // 이미 받침 있음
  const composed = String.fromCharCode(code + final);
  return out.slice(0, -1) + composed;
}

/** ン(받침 ㄴ)·ッ(다음 자음 강조: 무시)·ー(장음: 직전 모음 반복) 처리는 별도. */
function katakanaSegmentToHangul(seg: string): string {
  let out = '';
  for (let i = 0; i < seg.length; i += 1) {
    const ch = seg[i];
    const next = seg[i + 1];
    if (ch === 'ン') {
      // 받침 ㄴ — 직전 한글 음절에 합성
      out = appendBatchim(out, 4);
      continue;
    }
    if (ch === 'ッ') {
      // 촉음 — 한글에선 표현 제한적, 생략.
      continue;
    }
    if (ch === 'ー') {
      // 장음 부호 — 한국어는 장음 표기 안 함 → 생략.
      continue;
    }
    // 요음/외래어 결합 (キャ, シュ, ファ, ティ 등) — 모든 작은 가타카나 체크
    if (next && /[ァィゥェォャュョ]/.test(next)) {
      const mapped = YOON_MAP[ch]?.[next];
      if (mapped) {
        out += mapped;
        i += 1;
        continue;
      }
    }
    const k = KANA_BASIC[ch] ?? KANA_DAKU[ch];
    if (k) {
      out += k;
    } else {
      out += ch; // 알 수 없는 문자 (한자/히라가나/특수) — 그대로
    }
  }
  return out;
}

/**
 * 입력 문자열에서 카타카나 런(run)만 골라 한글 음역.
 * 비-카타카나 부분(영문/숫자/한자/히라가나)은 그대로 유지.
 */
function transliterateKatakanaRuns(input: string): string {
  // ゠-ヿ + 반각 카타카나 ｦ-ﾟ + ーー
  return input.replace(/[゠-ヿｦ-ﾟ]+/g, (run) =>
    katakanaSegmentToHangul(run),
  );
}

/** 히라가나 흔한 조사 한국어화 (단어 의미는 손상 가능 — 한글 표기만 목적). */
function hiraganaJoshiToHangul(input: string): string {
  return input
    .replace(/の/g, '의')
    .replace(/と/g, '와')
    .replace(/より/g, '보다')
    .replace(/から/g, '부터')
    .replace(/まで/g, '까지')
    .replace(/っ/g, '');
}

export function localizeCardName(input: string | null | undefined): string {
  if (!input) return '';
  let out = String(input);
  // 1) 사전 매칭 (브랜드/인지도 우선) — 가장 긴 매치 우선
  for (const [re, ko] of DICT) out = out.replace(re, ko);
  // 2) 흔한 히라가나 조사 (の 등) 변환
  out = hiraganaJoshiToHangul(out);
  // 3) 남은 카타카나는 한글로 음역 (사전에 없는 카드명도 한글 발음으로)
  out = transliterateKatakanaRuns(out);
  return out;
}
